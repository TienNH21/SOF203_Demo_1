package sof203;

/**
 *
 * @author tiennh
 */
public class Lop {
    private int id;
    private String ma;
    private int nienKhoa;

    public Lop() {
    }

    public Lop(int id, String ma, int nienKhoa) {
        this.id = id;
        this.ma = ma;
        this.nienKhoa = nienKhoa;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getMa() {
        return ma;
    }

    public void setMa(String ma) {
        this.ma = ma;
    }

    public int getNienKhoa() {
        return nienKhoa;
    }

    public void setNienKhoa(int nienKhoa) {
        this.nienKhoa = nienKhoa;
    }
    
    
    
}
